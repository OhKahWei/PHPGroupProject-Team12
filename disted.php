<h1>About DISTED College </h1>
</div>
<p> 
    Founded in 1985, DISTED College is Penang’s very first private tertiary institution that is known as a 
    premier not-for-profit private college and is currently owned by the Wawasan Education Foundation. 
	DISTED aims at being strongly committed to offering quality education and training to students for them to serve the needs of the industry and community.
    <br><br>
    DISTED College offers an impressive range of quality industry-relevant programmes for students to choose from, 
	and this includes Pre-University level to Bachelor Degrees and even Professional Qualifications in Business, Hospitality, Psychology, Computing & IT, Multimedia, 
	and Accounting. With its growing reputation, DISTED College is recognized as a provider of high-quality programmes and brilliant education experience with 5-Star accolades 
	awarded by the Malaysian Quality Evaluation System for Private Colleges (MyQUEST) 2014/2015 that was conducted by the Ministry of Education.
</p>
